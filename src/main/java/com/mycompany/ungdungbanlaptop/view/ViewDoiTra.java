    private void cbbNhanVien(){
        DefaultComboBoxModel boxModelNV = new DefaultComboBoxModel();
        cbbTenKhachHang.setModel(boxModelNV);
        for (KhachHang x : listKH) {
            boxModelNV.addElement(x.getHoTen());
        }
        
        
    }
    private void cbbKhachHang(){
        DefaultComboBoxModel boxModelKH = new DefaultComboBoxModel();
        cbbTenNhanVien.setModel(boxModelKH);
        for (NhanVien x : listNV) {
            boxModelKH.addElement(x.getHoTen());
        }
        
        
    }
    private void showData(List<DoiTRa> list){
        DefaultTableModel defaultTableModel = new DefaultTableModel();
        
        jtable_hodon.setModel(defaultTableModel);
        String a[] ={"Tên KH","Tên NV","Ngày Bắt Đầu","Ngày két thúc","lí do"};
        defaultTableModel.setColumnIdentifiers(a);
        defaultTableModel.setRowCount(0);
        for (DoiTRa x : list) {
            defaultTableModel.addRow(new Object[]{x.getKhachHang().getHoTen(),x.getNhanVien().getHoTen(),x.getNgayBatDau(),x.getNgayKetThuc(),x.getLiDo()});
        }
        
    }

