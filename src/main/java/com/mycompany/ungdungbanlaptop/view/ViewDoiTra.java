    private void btn_taohoadonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_taohoadonActionPerformed
        // TODO add your handling code here:
        DoiTRa doiTRa = new  DoiTRa();
        if(rbDoiHang.isSelected()){
             doiTRa.setHinhThucDoiTra(EnumHinhThucDoiTra.DOI_HANG);
        }else{
             doiTRa.setHinhThucDoiTra(EnumHinhThucDoiTra.TRA_HANG);
        }
        doiTRa.setKhachHang(khachHangService.getByTen(cbbTenKhachHang.getSelectedItem().toString()));
        doiTRa.setNhanVien(nhanVienService.getByTen(cbbTenNhanVien.getSelectedItem().toString()));
        doiTRa.setTienThu(BigDecimal.valueOf(Double.valueOf(txt_tienthu.getText())));
        doiTRa.setTienTra(BigDecimal.valueOf(Double.valueOf(txt_tientra.getText())));
        doiTRa.setLiDo(txt_lydo.getText());
        if(rbDaGiao.isSelected()){
            doiTRa.setTrangThai(0);
        }else{
             doiTRa.setTrangThai(1);
        }
        Date ngayBatDau = txt_ngaybatdau.getDate();
        String nbd = new ConverDate().convertDateToString(ngayBatDau, "dd/MM/yyyy");
        doiTRa.setNgayBatDau(new ConverDate().dateToLong(nbd, "dd/MM/yyyy"));
        
         Date ngayKetThuc = txt_ngayketthuc.getDate();
        String nkt = new ConverDate().convertDateToString(ngayKetThuc, "dd/MM/yyyy");
        doiTRa.setNgayKetThuc(new ConverDate().dateToLong(nkt, "dd/MM/yyyy"));
        doiTRa.setTinhTrang(1);
        doiTRa.setMoTa(txt_mota.getText());
        doiTRa.setHoaDonChiTiet(null);
        JOptionPane.showMessageDialog(this, doiTraService.add(doiTRa));
        showData(doiTraService.gelAll());
    }//GEN-LAST:event_btn_taohoadonActionPerformed


